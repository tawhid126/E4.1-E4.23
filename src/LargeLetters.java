
public class LargeLetters {
    public static void main(String[] args) {
        final String LETTER_H = "* *%n* *%n*****%n";
        final String LETTER_E = "*****%n*    %n*****%n*    %n*****%n";
        final String LETTER_L = "*    %n*    %n*    %n*    %n*****%n";
        final String LETTER_O = " *****%n*     *%n*     *%n*     *%n *****%n";

        System.out.printf(LETTER_H);
        System.out.printf(LETTER_E);
        System.out.printf(LETTER_L);
        System.out.printf(LETTER_L);
        System.out.printf(LETTER_O);
    }
}